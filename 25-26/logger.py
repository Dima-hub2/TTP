"""
СИСТЕМА ЛОГГИРОВАНИЯ
Записывает события в файл и выводит в консоль.
Важно для отладки и мониторинга работы бота.
"""

import logging

# ========== НАСТРОЙКА ЛОГГИРОВАНИЯ ==========

# basicConfig() настраивает систему логгирования один раз при запуске
logging.basicConfig(
    level=logging.INFO,  # Уровень логирования: INFO и выше (WARNING, ERROR, CRITICAL)
    format="%(asctime)s - %(message)s",  # Формат записи: "2024-01-01 12:00:00 - Сообщение"
    filename="bot.log"  # Файл для записи логов (создается автоматически)
)

# ========== ФУНКЦИИ ЛОГГИРОВАНИЯ ==========

def log(msg):
    """
    Запись информационного сообщения в лог
    Используется для обычных событий: запуск, ввод данных, расчеты
    """
    logging.info(msg)  # Запись в файл
    print(f"[LOG] {msg}")  # Вывод в консоль

def error(msg):
    """
    Запись сообщения об ошибке в лог
    Используется для ошибок, исключений, проблем
    """
    logging.error(msg)  # Запись в файл с уровнем ERROR
    print(f"[ERROR] {msg}")  # Вывод в консоль